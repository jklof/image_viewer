from PySide6.QtCore import QByteArray
from PySide6.QtGui import QIcon, QPixmap, QPainter, QColor
from PySide6.QtSvg import QSvgRenderer

# --- Helper Function ---


def create_icon(svg_data: str, color: str = "#ececec", size: int = 24) -> QIcon:
    """
    Converts an SVG string into a QIcon.
    Replaces 'currentColor' in the SVG with the specified hex color.
    """
    # Inject the desired color
    processed_svg = svg_data.replace("currentColor", color)

    renderer = QSvgRenderer(QByteArray(processed_svg.encode("utf-8")))
    pixmap = QPixmap(size, size)
    pixmap.fill(QColor(0, 0, 0, 0))  # Transparent background

    painter = QPainter(pixmap)
    renderer.render(painter)
    painter.end()

    return QIcon(pixmap)


